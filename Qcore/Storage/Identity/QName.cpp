#include "QName.h"
using namespace Q;

//------------------------------------------------------------------------------------------------------------------
Name::Name (const char* acName)
    :
    m_qName(acName)
{
}
//------------------------------------------------------------------------------------------------------------------
void Name::SetName (const String& rqName)
{
    m_qName = rqName;
}
//------------------------------------------------------------------------------------------------------------------
void Name::Save (Stream& rqStream) const
{
    m_qName.Save(rqStream);
}
//------------------------------------------------------------------------------------------------------------------
void Name::Load (Stream& rqStream)
{
    m_qName.Load(rqStream);
}
